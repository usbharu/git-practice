export const add = (a: number, b: number): number => {
    return a + b;
};

export const sub = (a: number, b: number): number => {
    return a - b;
}

export const div = (a: number, b: number): number => {
    return a / b;
};

export const mult = (a: number, b: number): number => {
    return a * b;
}